package pojo;

public class Employee {

    public String getEmployee_real_name() {
        return employee_real_name;
    }
    public void setEmployee_real_name(String employee_real_name) {
        this.employee_real_name = employee_real_name;
    }
    public String getEmployee_sex() {
        return employee_sex;
    }
    public void setEmployee_sex(String employee_sex) {
        this.employee_sex = employee_sex;
    }
    public String getEmployee_birthday() {
        return employee_birthday;
    }
    public void setEmployee_birthday(String employee_birthday) {
        this.employee_birthday = employee_birthday;
    }
    public String getEmployee_telephone() {
        return employee_telephone;
    }
    public void setEmployee_telephone(String employee_telephone) {
        this.employee_telephone = employee_telephone;
    }


    private String employee_real_name;
    private String employee_sex;
    private String employee_birthday;
    private String employee_telephone;

}
